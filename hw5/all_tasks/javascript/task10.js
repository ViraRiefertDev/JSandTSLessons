// Задача 10. Знайти суму всіх непарних чисел, що знаходяться між заданими користувачем числам
if (confirm('Почати тестування?')) {
  const high = parseInt(prompt('Введіть верхню границю діапазону'))
  const low = parseInt(prompt('Введіть нижню границю діапазону'))
  let sum = 0;
  for(let i=low; i<=high;i++){
    if(i%2){
      sum += i;
    }
  }
  document.write(`Сума всіх непарних чисел в діапазоні від ${low} до ${high}: ${sum}`)
}